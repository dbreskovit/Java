    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JTxtConta.getText().isEmpty() || JTxtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha os campos para continuar!");
        } else {
            MenuConta telaConta = new MenuConta();

            Inicio.lista.forEach((conta) -> {
                if (conta.getConta().equals(JTxtConta.getText()) && conta.getSenha().equals(JTxtSenha.getText())) {
                    this.logado = true;
                    telaConta.setConta(conta);
                }
            });

            if (this.logado == true) {
                telaConta.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Conta ou senha inv√°lida!!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

