    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double totalProduto = Double.parseDouble(jTextQntd.getText()) * produtoAtual.getValor();
        this.total += totalProduto;
        jLabelTotal.setText("R$ " + String.format("%.2f", this.total));
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        Object[] dados = {produtoAtual.getCod(), produtoAtual.getProduto(), produtoAtual.getValor(), Double.parseDouble(jTextQntd.getText()), totalProduto};
        table.addRow(dados);
        jButton4.setEnabled(true);
        jButton5.setEnabled(true);
        jTextProduto.setText("");
        jTextQntd.setText("");
        jLabelProduto.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

