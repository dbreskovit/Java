    private void jTextClienteKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextClienteKeyReleased
        String id = jTextCliente.getText();
        tempCliente(new Cliente(0, "", "", ""));

        App.clientes.forEach((cliente) -> {
            if (id == null ? cliente.getCod() + "" == null : id.equals(cliente.getCod() + "")) {
                tempCliente(cliente);
            }
        });

        if (this.cliente.getCod() != 0) {
            jLabelCliente.setText(this.cliente.getCod() + " - " + this.cliente.getNome());
        } else {
            jLabelCliente.setText("Null");
            tempCliente(new Cliente(0, "", "", ""));
        }
    }//GEN-LAST:event_jTextClienteKeyReleased

    private void jTextProdutoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextProdutoKeyReleased
        String id = jTextProduto.getText();
        tempProduto(new Produto(0, "", 0, ""));

        App.produtos.forEach((produto) -> {
            if (id == null ? produto.getCod() + "" == null : id.equals(produto.getCod() + "")) {
                tempProduto(produto);
            }
        });

        if (this.produtoAtual.getCod() != 0) {
            jLabelProduto.setText(this.produtoAtual.getCod() + " - " + this.produtoAtual.getProduto() + "");
        } else {
            jLabelProduto.setText("Null");
            tempProduto(new Produto(0, "", 0, ""));
        }
    }//GEN-LAST:event_jTextProdutoKeyReleased

